static int othe_yomiutu(char *ban, int k, int pos)
{
	int i, j, s, p, dir, kaiho;
	
	s = macroinv(k);
	kaiho = 0;
	for(i=0; i<8; i++){
		dir = othe_dir3[i];
		if(othe_yomitst_s(ban, k, pos, dir)){	// dir ï¿½ï¿½ï¿½ï¿½ï¿½Å‘Å‚Ä‚é‚©ï¿½H
			p = pos;
			for(;;){
				p += dir;
				if(ban[p]==s){
					ban[p] = k;
					for(j=0; j<8; j++){
						if(ban[p+othe_dir3[j]]==ISHI_N)
							++kaiho;
					}
				}
				else{
					break;
				}
			}
		}
	}
	ban[pos] = k;
	return kaiho;
}
