static int othe_yomitst_s(char *ban, int k, int pos, int dir)
{
	int kk;
	
	if(ban[pos])							// ï¿½Î–ï¿½ï¿½ï¿½ï¿½Å‚È‚ï¿½ï¿½ï¿½ï¿½
		return 0;							// ï¿½Å‚Ä‚È‚ï¿½
	pos += dir;
	if(ban[pos] != macroinv(k))				// ï¿½Gï¿½ï¿½ÉÚGï¿½ï¿½ï¿½Ä‚ï¿½ï¿½È‚ï¿½ï¿½ï¿½ï¿½
		return 0;							// ï¿½Å‚Ä‚È‚ï¿½
	for(;;){
		pos += dir;
		kk = ban[pos];
		if(kk==k)							// ï¿½Í‚ï¿½ï¿½ï¿½Å‚ï¿½ï¿½ï¿½
			return 1;						// ï¿½Å‚Ä‚ï¿½
		if(kk==ISHI_N || kk== ISHI_O)
			return 0;						// ï¿½Å‚Ä‚È‚ï¿½
	}
}

static int othe_yomitst(char *ban, int k, int pos)
{
	int i;
	
	if(ban[pos])							// ï¿½Î–ï¿½ï¿½ï¿½ï¿½Å‚È‚ï¿½ï¿½ï¿½ï¿½
		return 0;							// ï¿½Å‚Ä‚È‚ï¿½
	for(i=0; i<8; i++){
		if(othe_yomitst_s(ban, k, pos, othe_dir3[i]))	// dir3 ï¿½ï¿½ï¿½ï¿½ï¿½Å‘Å‚Ä‚é‚©ï¿½H
			return 1;						// ï¿½Å‚Ä‚ï¿½
	}
	return 0;								// ï¿½Å‚Ä‚È‚ï¿½
}

